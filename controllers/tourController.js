exports.getTourStats = async (req, res) => {
  try {
    const stats = await Tour.aggregate([
      {
        $match: { ratingsAverage: { $gte: 4.5 } },
      },
      {
        $group: {
          _id: { $toUpper: '$difficulty' },
          numTours: { $sum: 1 },
          numRatings: { $sum: '$ratingsQuantity' },
          avgRating: { $avg: '$ratingsAverage' },
          avgPrice: { $avg: '$price' },
          minPrice: { $min: '$price' },
          maxPrice: { $max: '$price' },
        },
      },
      {
        $sort: { avgPrice: -1 },
      },
      // {
      //   $match: { _id: { $ne: 'EASY' } },
      // },
    ]);

    res.status(200).json({
      status: 'success',
      data: {
        stats,
      },
    });
  } catch (error) {
    res.status(404).json({
      status: 'fail',
      message: error,
    });
  }
};

exports.getMonthlyPlan = async (req, res) => {
  try {
    const year = +req.params.year;
    const plan = await Tour.aggregate([
      {
        // before we have 9 tours, each one has 3 [startDates]
        // unwind with startDates
        // after we have 27 tours, each one has 1 [startDates]
        $unwind: '$startDates',
      },
      {
        $match: {
          startDates: {
            $gte: new Date(`${year}-01-01`),
            $lte: new Date(`${year}-12-31`),
          },
        },
      },
      {
        $group: {
          // group them by the month , so there will be 12 groups
          _id: { $month: '$startDates' },
          // count how many datas in each group
          numTourStarts: { $sum: 1 },
          // shows the names of each group (array)
          // "tours": ["The Forest Hiker","The Wine Taster"]
          tours: { $push: '$name' },
        },
      },
      {
        // "month": 5
        $addFields: { month: '$_id' },
      },
      {
        $project: {
          // hide _id info, if set to 1, data will be show
          _id: 0,
        },
      },
      {
        // sort the data by descent
        $sort: { numTourStarts: -1 },
      },
      {
        // only shows 12 data
        $limit: 12,
      },
    ]);

    res.status(200).json({
      status: 'success',
      data: {
        plan,
      },
    });
  } catch (error) {
    res.status(404).json({
      status: 'fail',
      message: error,
    });
  }
};
